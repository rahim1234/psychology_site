def calculate_mcmi4_scale_scores(answers: dict) -> dict:
    """محاسبه نمرات خام MCMI-4 برای همه مقیاس‌ها (ساده‌شده)."""
    # این تابع ساده‌شده فقط نمره کل را محاسبه می‌کند
    # برای تحلیل دقیق مقیاس‌ها نیاز به manual رسمی دارید
    total_score = sum(int(answers.get(f'q{i}', 0)) for i in range(1, 196))
    return {
        'total': total_score,
        'max_possible': 195,
        'percentage': round(total_score / 195 * 100, 1),
    }

def interpret_mcmi4_results(raw_scores: dict) -> dict:
    """تفسیر ساده نتایج MCMI-4 (غربالگری)"""
    total = raw_scores.get('total', 0)
    
    interpretation = {
        'total_score': total,
        'level': 'normal',
        'recommendations': [],
        'clinical_flags': [],
    }
    
    # تفسیر ساده بر اساس درصد
    percentage = raw_scores.get('percentage', 0)
    
    if percentage >= 70:
        interpretation['level'] = 'severe'
        interpretation['clinical_flags'].append('نمرات بالا - نیاز به ارزیابی بالینی جامع')
        interpretation['recommendations'].append('ارزیابی فوری توسط روانشناس بالینی توصیه می‌شود')
    elif percentage >= 50:
        interpretation['level'] = 'moderate'
        interpretation['clinical_flags'].append('نمرات متوسط - نیاز به بررسی بیشتر')
        interpretation['recommendations'].append('مشاوره با متخصص بهداشت روان توصیه می‌شود')
    elif percentage >= 30:
        interpretation['level'] = 'mild'
        interpretation['recommendations'].append('پیگیری وضعیت و در صورت نیاز مشورت با متخصص')
    else:
        interpretation['level'] = 'normal'
        interpretation['recommendations'].append('وضعیت طبیعی - ادامه سبک زندگی سالم')
    
    return interpretation